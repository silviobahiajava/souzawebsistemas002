package br.com.souzawebsistemas.model;

import java.util.Date;

public class CertificadoDeApresentacao {
	private String nomeDoRecemNascido;
	private Date dataDeNascimentoDoRecemNascido;
	private String nomeDoPai;
	private String nomeDaMae;
	private String nomepastorLocal;
	private String diaDeApresentacao;
	private String mesDeApresentacao;
	private String anoDeApresentacao;
	public String getNomeDoRecemNascido() {
		return nomeDoRecemNascido;
	}
	public void setNomeDoRecemNascido(String nomeDoRecemNascido) {
		this.nomeDoRecemNascido = nomeDoRecemNascido;
	}
	public Date getDataDeNascimentoDoRecemNascido() {
		return dataDeNascimentoDoRecemNascido;
	}
	public void setDataDeNascimentoDoRecemNascido(Date dataDeNascimentoDoRecemNascido) {
		this.dataDeNascimentoDoRecemNascido = dataDeNascimentoDoRecemNascido;
	}
	public String getNomeDoPai() {
		return nomeDoPai;
	}
	public void setNomeDoPai(String nomeDoPai) {
		this.nomeDoPai = nomeDoPai;
	}
	public String getNomeDaMae() {
		return nomeDaMae;
	}
	public void setNomeDaMae(String nomeDaMae) {
		this.nomeDaMae = nomeDaMae;
	}
	public String getNomepastorLocal() {
		return nomepastorLocal;
	}
	public void setNomepastorLocal(String nomepastorLocal) {
		this.nomepastorLocal = nomepastorLocal;
	}
	public String getDiaDeApresentacao() {
		return diaDeApresentacao;
	}
	public void setDiaDeApresentacao(String diaDeApresentacao) {
		this.diaDeApresentacao = diaDeApresentacao;
	}
	public String getMesDeApresentacao() {
		return mesDeApresentacao;
	}
	public void setMesDeApresentacao(String mesDeApresentacao) {
		this.mesDeApresentacao = mesDeApresentacao;
	}
	public String getAnoDeApresentacao() {
		return anoDeApresentacao;
	}
	public void setAnoDeApresentacao(String anoDeApresentacao) {
		this.anoDeApresentacao = anoDeApresentacao;
	}
	

}
